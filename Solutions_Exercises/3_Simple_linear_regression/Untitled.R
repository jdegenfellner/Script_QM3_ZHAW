# pre post points when guessing

pre <- rnorm(100, 100, 15)
post <- rnorm(100, 100, 15)

df <- data.frame(pre, post)
df$high_performer <- ifelse(df$pre > quantile(df$pre, c(0.9)), 1, 0)

df %>% 
  ggplot(aes(x=post, y=pre, color=high_performer)) +
  geom_point() +
  geom_abline(intercept = 0, slope = 1, linetype = "dashed") +
  theme_minimal() +
  labs(title = "Pre vs Post scores",
       x = "Post",
       y = "Pre") +
  theme(legend.position = "none") + 
  theme(plot.title = element_text(hjust = 0.5))


















































































































































































































































































































































































































































































































































































































